// 연산자 (+, -, *, /, %)

//1. 더하기 연산자
console.log(1 + 1); // 2
console.log(1 + "1"); // 11. 문자열로 변환

// 2. 빼기 연산자
console.log(1 - "2"); // -1
console.log(1 - 2); // -1

//3. 곱하기 연산자(*)
console.log(2 * 3); // 6
console.log("2" * 3); // 6

//4. 나누기 연산자 (/)
console.log(4 / 2); // 2
console.log("4" / 2); // 2

// 5. 나누기 연산자(/) vs  나머지 연산자(%)
console.log(5 / 2); // 2.5
console.log(5 % 2); // 1
